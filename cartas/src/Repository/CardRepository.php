    public function gameCards($number)
    {
        $cards = $this -> findAll();
        $selectedCards = [];

        for ($i = 1; $i <= $number; $i++){
            $find = false;

            do {
                $key = array_rand($cards); //obtiene un Ã­ndice

                $card = $cards[$key]; 
                unset($cards[$key]); 
    
                $selectedCards[] = $card;
    
                $find = true;
            } while (!$find);

        }
        return $selectedCards;
    }

